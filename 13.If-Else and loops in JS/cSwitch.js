
switch(new Date().getDay()){
    case 0:{
        console.log("Sunday");
        break;
    }
    case 1:{
        console.log("Monday");
        break;
    }
    case 2:{
        console.log("Tuesday");
        break;
    }
    case 3:{
        console.log("Wednesday");
        break;
    }
    case 4:{
        console.log("thursday");
        break;
    }
    case 5:{
        console.log("friday");
        break;
    }
    case 6:{
        console.log("Saturday");
        break;
    }
    default:{
        console.log("Not a day!");
    }
}